"""#Symptom Network Analysis"""

from scipy import stats
import networkx as nx
import matplotlib.pyplot as plt
import matplotlib.patches as mpatches
import numpy as np
from collections import defaultdict

# ---- 3A: Compute Spearman Correlation Matrix ----
corr_matrix = X.corr(method='spearman')

# ---- 3B: Build Network (threshold |r| > 0.30) ----
THRESHOLD = 0.30
G = nx.Graph()
G.add_nodes_from(SYMPTOM_COLS)

edges = []
for i, col1 in enumerate(SYMPTOM_COLS):
    for j, col2 in enumerate(SYMPTOM_COLS):
        if i < j:
            r = corr_matrix.loc[col1, col2]
            if abs(r) > THRESHOLD:
                G.add_edge(col1, col2, weight=abs(r), r=r)
                edges.append({'source': col1, 'target': col2, 'weight': abs(r), 'r': round(r, 3)})

edges_df = pd.DataFrame(edges).sort_values('weight', ascending=False)
print(f"Total significant edges (|r|>{THRESHOLD}): {len(edges_df)}")
print(f"Cross-domain edges: {len(edges_df[edges_df.source.str[0] != edges_df.target.str[0]])}")

# ---- 3C: Centrality Measures ----
degree_centrality    = nx.degree_centrality(G)
strength_centrality  = {n: sum(d['weight'] for _, _, d in G.edges(n, data=True)) for n in G.nodes()}
betweenness          = nx.betweenness_centrality(G, weight='weight')

centrality_df = pd.DataFrame({
    'symptom':     SYMPTOM_COLS,
    'degree':      [degree_centrality[n] for n in SYMPTOM_COLS],
    'strength':    [strength_centrality[n] for n in SYMPTOM_COLS],
    'betweenness': [betweenness[n] for n in SYMPTOM_COLS],
    'domain':      ['Anxiety' if n.startswith('A') else 'Stress' if n.startswith('S') else 'Depression'
                    for n in SYMPTOM_COLS]
}).sort_values('strength', ascending=False)

print("\nTop 10 Most Central Symptoms:")
print(centrality_df[['symptom', 'domain', 'strength', 'betweenness']].head(10).to_string(index=False))

# ---- 3D: Visualize Network ----
fig, ax = plt.subplots(1, 1, figsize=(16, 12))

# Color nodes by domain
node_colors = []
for n in G.nodes():
    if n.startswith('A'):
        node_colors.append('#3498db')   # Blue = Anxiety
    elif n.startswith('S'):
        node_colors.append('#e67e22')   # Orange = Stress
    else:
        node_colors.append('#e74c3c')   # Red = Depression

# Size by strength
node_sizes = [strength_centrality[n] * 200 for n in G.nodes()]

# Edge width by correlation strength
edge_widths = [G[u][v]['weight'] * 3 for u, v in G.edges()]

# Layout: group by domain
pos = {}
a_nodes = [n for n in SYMPTOM_COLS if n.startswith('A')]
s_nodes = [n for n in SYMPTOM_COLS if n.startswith('S')]
d_nodes = [n for n in SYMPTOM_COLS if n.startswith('D')]

for i, n in enumerate(a_nodes):
    pos[n] = (-3, i - len(a_nodes)/2)
for i, n in enumerate(s_nodes):
    pos[n] = (0, i - len(s_nodes)/2)
for i, n in enumerate(d_nodes):
    pos[n] = (3, i - len(d_nodes)/2)

# Draw cross-domain edges in different color
within_edges  = [(u, v) for u, v in G.edges() if u[0] == v[0]]
cross_edges   = [(u, v) for u, v in G.edges() if u[0] != v[0]]

nx.draw_networkx_edges(G, pos, edgelist=within_edges, width=1.5,
                       alpha=0.3, edge_color='gray', ax=ax)
nx.draw_networkx_edges(G, pos, edgelist=cross_edges, width=2.5,
                       alpha=0.7, edge_color='#9b59b6', ax=ax)
nx.draw_networkx_nodes(G, pos, node_color=node_colors,
                       node_size=node_sizes, alpha=0.9, ax=ax)
nx.draw_networkx_labels(G, pos, font_size=7, ax=ax)

# Legend
patches = [
    mpatches.Patch(color='#3498db', label='Anxiety (GAD-7)'),
    mpatches.Patch(color='#e67e22', label='Stress (PSS-10)'),
    mpatches.Patch(color='#e74c3c', label='Depression (PHQ-9)'),
    mpatches.Patch(color='#9b59b6', label='Cross-domain bridge'),
]
ax.legend(handles=patches, loc='upper right', fontsize=11)
ax.set_title('Academic Pressure Symptom Network\n26 Items, Spearman |r|>0.30, Node size = Centrality',
             fontweight='bold', fontsize=13)
ax.axis('off')
plt.tight_layout()
plt.savefig('fig4_symptom_network.png', dpi=300, bbox_inches='tight')
plt.show()
print("Saved: fig4_symptom_network.png")

