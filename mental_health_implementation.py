# Bootstrap stability analysis for Bayesian Network edges

from pgmpy.estimators import HillClimbSearch
import numpy as np

print("Bootstrap stability analysis (50 iterations)...")
edge_counts = {}
n_bootstrap = 50

for i in range(n_bootstrap):
    sample = X.sample(frac=0.8, replace=True, random_state=i)
    hc = HillClimbSearch(data=sample)
    model = hc.estimate(scoring_method='bic-g', show_progress=False,
                       max_indegree=3, max_iter=500)
    for edge in model.edges():
        edge_counts[edge] = edge_counts.get(edge, 0) + 1

# Stable edges (appear in >60% of bootstrap samples)
stable_edges = {e: c for e, c in edge_counts.items() if c/n_bootstrap > 0.60}
print(f"\nStable edges (>60% bootstrap): {len(stable_edges)}")
for edge, count in sorted(stable_edges.items(), key=lambda x: x[1], reverse=True):
    cross = " [CROSS]" if edge[0][0] != edge[1][0] else ""
    print(f"  {edge[0]} → {edge[1]}: {count/n_bootstrap*100:.0f}%{cross}")

